package com.apro.MaherQuickBetAnalyzer;

public class RectangleData {
    public int x, y, width, height;
    public String name;

    public RectangleData(int x, int y, int width, int height, String name) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.name = name;
    }
}
